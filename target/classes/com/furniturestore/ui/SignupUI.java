     private void signupActionPerformed(java.awt.event.ActionEvent evt) {
        String username = jTextField1.getText();
    String email = jTextField2.getText();
    String password = new String(jPasswordField1.getPassword());
    String confirmPassword = new String(jPasswordField2.getPassword());

    if (!password.equals(confirmPassword)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match.", "Signup Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try (BufferedWriter writer = new BufferedWriter(new FileWriter("users.txt", true))) {
        // Append user information to the file
        writer.write(username + "  " + password + "  " + email);
        writer.newLine();
        writer.flush();
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Failed to write user information to file.", "Signup Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Notify user about successful signup
    JOptionPane.showMessageDialog(this, "Signup successful. You can now login.", "Signup Success", JOptionPane.INFORMATION_MESSAGE);

    // Open the login window
    LoginUI loginUI = new LoginUI();
    loginUI.setVisible(true);
    this.dispose();
    }

