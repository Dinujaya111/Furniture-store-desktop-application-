    private void validateLogin() {
    String enteredUsername = jTextField1.getText();
    String enteredPassword = new String(jPasswordField1.getPassword());

    // Hardcoded credentials
    String adminUsername = "admin";
    String adminPassword = "admin123"; // Reminder: Hash passwords in a real application

    // Check if the entered credentials match admin credentials
    if (enteredUsername.equals(adminUsername) && enteredPassword.equals(adminPassword)) {
        // If admin credentials, go to AdminDashboardUI
        isAdmin = true;
        AdminDashboardUI adminDashboard = new AdminDashboardUI();
        adminDashboard.setVisible(true);
        this.dispose();
        return;
    }

    // For non-admin users, check if username exists and password matches
    File file = new File("users.txt");
    try (Scanner scanner = new Scanner(file)) {
        while (scanner.hasNextLine()) {
            String[] credentials = scanner.nextLine().split("  "); // Assuming a two-space separator
            if (credentials[0].equals(enteredUsername) && credentials[1].equals(enteredPassword)) {
                // If user credentials, go to SellerDashboardUI
                isAdmin = false;
                SellerDashboardUI sellerDashboard = new SellerDashboardUI();
                sellerDashboard.setVisible(true);
                this.dispose();
                
                
                return;
            }
        }
        // If no match found, show error message
        JOptionPane.showMessageDialog(this, "Invalid username or password.", "Login Failed", JOptionPane.ERROR_MESSAGE);
    } catch (FileNotFoundException e) {
        JOptionPane.showMessageDialog(this, "The authentication file is missing.", "Login Error", JOptionPane.ERROR_MESSAGE);
    }
    
    

    }

