    private void addDeleteButton() {
    JButton deleteButton = new JButton("Delete");
    deleteButton.addActionListener(new ActionListener() {
        public void actionPerformed(ActionEvent e) {
            int selectedRow = userTable.getSelectedRow();
            if (selectedRow != -1) {
                deleteUser(selectedRow);
            }
        }
    });
    jPanel1.add(deleteButton, BorderLayout.SOUTH);
}
    
    private void displayUserData() {
        String filePath = "users.txt";
        String line;
        String[] columnNames = {"Username", "Email"};
        tableModel = new DefaultTableModel(columnNames, 0);

        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            while ((line = br.readLine()) != null) {
                String[] parts = line.split("\\s+");
                if (parts.length >= 2) {
                    String username = parts[0];
                    String email = parts[parts.length - 1];
                    tableModel.addRow(new Object[]{username, email});
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        userTable = new JTable(tableModel);
        JScrollPane scrollPane = new JScrollPane(userTable);

        jPanel1.setLayout(new BorderLayout());
        jPanel1.add(scrollPane, BorderLayout.CENTER);
        jPanel1.revalidate();

        // Add mouse listener to handle row selection after initComponents() is called
        userTable.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int row = userTable.rowAtPoint(e.getPoint());
                int col = userTable.columnAtPoint(e.getPoint());
                if (row >= 0 && col >= 0) {
                    if (col == 0) {
                        editUser(row);
                    } else if (col == 1) {
                        deleteUser(row);
                    }
                }
            }
        });
    }

    private void editUser(int row) {
        String username = (String) userTable.getValueAt(row, 0);
        String email = (String) userTable.getValueAt(row, 1);

        // Create a popup dialog for editing user information
        JTextField usernameField = new JTextField(username);
        JTextField emailField = new JTextField(email);

        JPanel panel = new JPanel(new GridLayout(0, 1));
        panel.add(new JLabel("Username:"));
        panel.add(usernameField);
        panel.add(new JLabel("Email:"));
        panel.add(emailField);

        int result = JOptionPane.showConfirmDialog(null, panel, "Edit User",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (result == JOptionPane.OK_OPTION) {
            // Update the table with the edited information
            tableModel.setValueAt(usernameField.getText(), row, 0);
            tableModel.setValueAt(emailField.getText(), row, 1);
            // You can update the users.txt file here
        }
    }

    private void deleteUser(int row) {
    int result = JOptionPane.showConfirmDialog(null,
            "Are you sure you want to delete this user?", "Confirm Delete",
            JOptionPane.YES_NO_OPTION);
    if (result == JOptionPane.YES_OPTION) {
        // Delete the user from the table
        tableModel.removeRow(row);
        // You can update the users.txt file here
    }
}

    
